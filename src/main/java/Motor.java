public class Motor {
    private int numeroDeMotor, cmCubicos;

    public Motor(int numeroDeMotor, int cmCubicos) {
        this.numeroDeMotor = numeroDeMotor;
        this.cmCubicos = cmCubicos;
    }

    public int getNumeroDeMotor() {
        return numeroDeMotor;
    }

    public int getCmCubicos() {
        return cmCubicos;
    }
}
